package FIndingPalindrome;

public class Palindrome {

	public static void main(String[] args) {
				
		String str1="I am a programmer";
		String str2="";
		char ch;
		
		for(int i=0;i<str1.length();i++) {
			
			str1.toCharArray();
			
			ch=str1.charAt(i);
			str2=ch+str2;
		}
		
		System.out.println(str2);
	}

}
